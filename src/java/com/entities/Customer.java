package com.entities;

/**
 *
 * @author dung nguyen
 */
public class Customer {

    private String uid;
    private String pwd;
    private String mode;
    private String date;
    private int id;

    public Customer(String uid, String pwd, String mode, int id,String date) {
        this.uid = uid;
        this.pwd = pwd;
        this.mode = mode;
        this.date = date;
        this.id = id;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getPwd() {
        return pwd;
    }

    public void setPwd(String pwd) {
        this.pwd = pwd;
    }

    public String getMode() {
        return mode;
    }

    public void setMode(String mode) {
        this.mode = mode;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }





}
